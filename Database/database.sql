CREATE TABLE BUSINESS (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
  NAME VARCHAR(100),
  "ACTIVE" BOOLEAN DEFAULT TRUE,
  CONSTRAINT PK_BUSINESS PRIMARY KEY (ID)
);

CREATE TABLE USERS (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
  NAME VARCHAR(150),
  EMAIL VARCHAR(200),
  "PASSWORD" VARCHAR(128),
  "ACTIVE" BOOLEAN DEFAULT TRUE,
  ADMINISTRATOR BOOLEAN DEFAULT FALSE,
  BUSINESS_ID INTEGER NOT NULL,
  IMAGE_PATH VARCHAR(500),
  CONSTRAINT PK_USERS PRIMARY KEY (ID),
  CONSTRAINT FK_USERS_BUSINESS FOREIGN KEY (BUSINESS_ID) REFERENCES BUSINESS(ID) ON DELETE CASCADE
);

CREATE TABLE PRODUCT_CATEGORY (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
  NAME VARCHAR(100),
  "ACTIVE" BOOLEAN DEFAULT TRUE,
  CONSTRAINT PK_PRODUCT_CATEGORY PRIMARY KEY (ID)
);

CREATE TABLE PRODUCT (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
  CATEGORY_ID INTEGER NOT NULL,
  NAME VARCHAR(150),
  BARCODE VARCHAR(20),
  "ACTIVE" BOOLEAN DEFAULT TRUE,
  DESCRIPTION VARCHAR(500),
  PRICE FLOAT,
  CONSTRAINT PK_PRODUCT PRIMARY KEY(ID),
  CONSTRAINT FK_PRODUCT_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES PRODUCT_CATEGORY(ID)
);

CREATE TABLE PRODUCT_IMAGE (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
  PRODUCT_ID INTEGER NOT NULL,
  IMAGE_PATH VARCHAR(250),
  CONSTRAINT PK_PRODUCT_IMAGE PRIMARY KEY (ID),
  CONSTRAINT FK_PRODUCT_IMAGE FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT(ID) ON DELETE CASCADE
);

CREATE TABLE STOCK (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
  PRODUCT_ID INTEGER NOT NULL,
  BUSINESS_ID INTEGER NOT NULL,
  USER_ID INTEGER NOT NULL,
  OPERATION_DATE TIMESTAMP,
  QUANTITY FLOAT,
  OPERATION VARCHAR(20),
  CONSTRAINT PK_STOCK PRIMARY KEY (ID),
  CONSTRAINT FK_STOCK_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT(ID) ON DELETE CASCADE,
  CONSTRAINT FK_STOCK_BUSINESS FOREIGN KEY (BUSINESS_ID) REFERENCES BUSINESS(ID) ON DELETE CASCADE,
  CONSTRAINT FK_STOCK_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID)
);

